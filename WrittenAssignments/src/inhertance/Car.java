package inhertance;

public class Car {
	
	private int wheelNo,headlights,viper;

	public int getWheelNo() {
		return wheelNo;
	}

	public void setWheelNo(int wheelNo) {
		this.wheelNo = wheelNo;
	}

	public int getHeadlights() {
		return headlights;
	}

	public void setHeadlights(int headlights) {
		this.headlights = headlights;
	}

	public int getViper() {
		return viper;
	}

	public void setViper(int viper) {
		this.viper = viper;
	}
	
	
	
	
	
}
